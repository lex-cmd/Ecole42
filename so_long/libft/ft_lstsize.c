#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*rst;

	i = 0;
	if (!lst)
		return (0);
	rst = lst;
	while (rst)
	{
		rst = rst -> next;
		i++;
	}
	return (i);
}

#include "minishell.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	x;
	size_t	m;

	i = 0;
	x = 0;
	m = 1;
	while ((str[i] == '\t') || (str[i] == '\n') || (str[i] == '\v')
		|| (str[i] == '\f') || (str[i] == '\r') || (str[i] == ' '))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		m = m * -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		x = ((str[i] - 48) + (x * 10));
		i++;
	}
	return (x * m);
}

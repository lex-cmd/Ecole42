#include "minishell.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s || fd < 0)
		return ;
	while (s[i] && fd > 0)
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}

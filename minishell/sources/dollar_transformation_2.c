#include "minishell.h"

int	skip(char *str, int i)
{
	if (str[i] == 39)
	{
		i++;
		while (str[i] != 39)
			i++;
	}
	return (i);
}

int	symbols(char c)
{
	if ((c == '%') || (c == '>')
		|| (c == '<') || (c == '{') || (c == '}')
		|| (c == '|') || (c == '[') || (c == ']')
		|| (c == '+') || (c == '=') || (c == ':')
		|| (c == ' ') || (c == '.') || (c == ',')
		|| (c == ';') || (c == 0))
		return (1);
	return (0);
}

#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*sc;

	sc = (char *)s;
	i = 0;
	while (i < n)
	{
		*(sc + i) = 0;
		i++;
	}
}
